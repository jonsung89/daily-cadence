import CoreGraphics

/// DailyCadence corner-radius tokens.
///
/// `md` (10pt) is the system default for cards, inputs, and buttons.
/// `pill` is used for chips, avatar rings, and the FAB.
enum Radius {
    static let sm: CGFloat   = 6
    static let md: CGFloat   = 10
    static let lg: CGFloat   = 14
    static let pill: CGFloat = 999
}
